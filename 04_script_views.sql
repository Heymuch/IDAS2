CREATE OR REPLACE VIEW VW_USERS_STATUS AS
    SELECT * FROM USERS_STATUS;

CREATE OR REPLACE VIEW VW_USERS AS
    SELECT USER_ID, USERNAME, FIRST_NAME, MIDDLE_NAME, LAST_NAME, EMAIL, USERS.STATUS_ID, USERS_STATUS.NAME STATUS_NAME, ADMIN FROM USERS
        JOIN USERS_STATUS ON USERS.STATUS_ID = USERS_STATUS.STATUS_ID;

CREATE OR REPLACE VIEW VW_STUDENTS AS
    SELECT STUDENT_ID, STUDENTS.USER_ID, LAST_NAME || ', ' || FIRST_NAME || (CASE WHEN MIDDLE_NAME IS NOT NULL THEN ' ' || MIDDLE_NAME ELSE NULL END) NAME, EMAIL, YEAR, STATUS_ID, STATUS_NAME FROM STUDENTS
        JOIN VW_USERS ON VW_USERS.USER_ID = STUDENTS.USER_ID;